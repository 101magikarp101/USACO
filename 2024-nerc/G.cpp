template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int T, N;

int ask(string s) {
    cout << 1 << " " << s << endl;
    cout.flush();
    int x; cin >> x;
    return x;
}

void tell(int i, char c) {
    cout << 0 << " " << i << " " << c << endl;
    cout.flush();
    int x; cin >> x;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cin >> T;
    while (T--) {
        cin >> N;
        int o = ask("1");
        if (o==N) {
            tell(1, '1');
        } else if (o==0) {
            tell(1, '0');
        } else {
            int block = o-ask("11");
            int hm = ask("01");
            if (hm == block-1) {
                tell(1, '1');
            } else {
                tell(1, '0');
            }
        }
    }
    return 0;
}