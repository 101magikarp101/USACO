int solve(int x) {
    int ans = 0;
    while (x) {
        ans += x/2;
        x /= 2;
    }  
    return ans;
}

int single(int x) {
    int ans = 0;
    while (x) {
        if (x%2==0) ans++;
        else break;
        x /= 2;
    }
    return ans;
}

