template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int T;
ll a, b; 

bool solve(ll m) {
    ll x = a, y = b;
    for (ll i = m; i >= 1; i--) {
        if (x >= y) {
            x -= i;
        } else {
            y -= i;
        }
        if (x < 0 || y < 0) {
            return 0;
        }
    }
    return 1;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cin >> T;
    while (T--) {
        cin >> a >> b;
        ll l = 0, r = 1e6;
        while (l < r) {
            ll m = (l+r+1)/2;
            if (solve(m)) {
                l = m;
            } else {
                r = m-1;
            }
        }
        cout << l << endl;
    }
    return 0;
}