struct pii {
    int x, y;
    bool operator<(const pii &a) const { return x == a.x ? y < a.y : x < a.x; }
    bool operator>(const pii &a) const { return x == a.x ? y > a.y : x > a.x; }
    bool operator==(const pii &a) const { return x == a.x && y == a.y; }
    bool operator!=(const pii &a) const { return x != a.x || y != a.y; }
    pii operator+(const pii &a) const { return {x+a.x, y+a.y}; }
    pii operator-(const pii &a) const { return {x-a.x, y-a.y}; }
    pii operator*(const int &a) const { return {x*a, y*a}; }
    pii operator/(const int &a) const { return {x/a, y/a}; }
};
struct pll {
    ll x, y;
    bool operator<(const pll &a) const { return x == a.x ? y < a.y : x < a.x; }
    bool operator>(const pll &a) const { return x == a.x ? y > a.y : x > a.x; }
    bool operator==(const pll &a) const { return x == a.x && y == a.y; }
    bool operator!=(const pll &a) const { return x != a.x || y != a.y; }
    pll operator+(const pll &a) const { return {x+a.x, y+a.y}; }
    pll operator-(const pll &a) const { return {x-a.x, y-a.y}; }
    pll operator*(const ll &a) const { return {x*a, y*a}; }
    pll operator/(const ll &a) const { return {x/a, y/a}; }
};

int T, N;
int a[200005];

int ad(int a, int b) {
    a+=b;
    if (a>=MOD2) a-=MOD2;
    return a;
}

int sub(int a, int b) {
    a-=b;
    if (a<0) a+=MOD2;
    return a;
}

int mul(int a, int b) {
    return (int)((a*1LL*b)%MOD2);
}

int binpow(int a, int b) {
    int res = 1;
    while (b) {
        if (b&1) res = mul(res, a);
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}

int inv(int a) {
    return binpow(a, MOD2-2);
}

int di(int a, int b) {
    return mul(a, inv(b));
}

struct state {
    int x, y;
    double v;
    bool operator<(const state &a) const { return v < a.v; }
};

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cin >> T;
    while (T--) {
        cin >> N;
        for (int i = 1; i <= N; i++) {
            cin >> a[i];
        }
        mset<state> s;
        int tot = 0;
        for (int i = 1; i <= N; i++) {
            int t = 0;
            double v = log2(a[i]);
            while (!s.empty()) {
                state p = *s.begin();
                if (p.v > v) break;
                s.erase(s.begin());
                tot = sub(tot, mul(p.x, binpow(2, p.y)));
                tot = ad(tot, p.x);
                t += p.y;
                v += p.y;
            }
            while (a[i]%2==0) {
                a[i] /= 2;
                t++;
            }
            tot = ad(tot, mul(a[i], binpow(2, t)));
            s.insert({a[i], t, log2(a[i])});
            // cout << "s: ";
            // for (auto it = s.begin(); it != s.end(); it++) {
            //     cout << "(" << it->x << ", " << it->y << ") ";
            // }
            // cout << endl;
            cout << tot << " ";
        }
        cout << endl;
    }
    return 0;
}