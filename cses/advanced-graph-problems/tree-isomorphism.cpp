int mul(int a, int b) {
    return (int)((a*1LL*b)%MOD2);
}

int binpow(int a, int b) {
    int res = 1;
    while (b) {
        if (b&1) res = mul(res, a);
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}

int inv(int a) {
    return binpow(a, MOD2-2);
}

int di(int a, int b) {
    return mul(a, inv(b));
}

int T, N;

int hg(int u, int p, vvt<int> &adj) {
    int h = C;
    each(v, adj[u]) {
        if (v == p) continue;
        h = mul(h, hg(v, u, adj) + 1);
    }
    return h;
}

