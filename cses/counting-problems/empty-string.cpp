int ad(int a, int b) {
    a+=b;
    if (a>=MOD2) a-=MOD2;
    return a;
}

int sub(int a, int b) {
    a-=b;
    if (a<0) a+=MOD2;
    return a;
}

int mul(int a, int b) {
    return (int)((a*1LL*b)%MOD2);
}

int binpow(int a, int b) {
    int res = 1;
    while (b) {
        if (b&1) res = mul(res, a);
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}

int inv(int a) {
    return binpow(a, MOD2-2);
}

int di(int a, int b) {
    return mul(a, inv(b));
}

int N;
int dp[505][505];
int nck[505][505];

void pre(int n) {
    nck[0][0] = 1;
    for (int i = 1; i <= n; i++) {
        nck[i][0] = 1;
        for (int j = 1; j <= i; j++) {
            nck[i][j] = ad(nck[i-1][j-1], nck[i-1][j]);
        }
    }
}

