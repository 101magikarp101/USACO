int add(int a, int b, int M) {
    a += b;
    if (a >= M) a -= M;
    return a;
}

int sub(int a, int b, int M) {
    a -= b;
    if (a < 0) a += M;
    return a;
}

int mul(int a, int b, int M) {
    return (int)((a * 1LL * b) % M);
}

int binpow(int a, int b, int M) {
    int res = 1;
    while (b) {
        if (b & 1) res = mul(res, a, M);
        a = mul(a, a, M);
        b >>= 1;
    }
    return res;
}

int inv(int a, int M) {
    return binpow(a, M - 2, M);
}

