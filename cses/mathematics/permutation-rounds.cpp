template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int ad(int a, int b) {
    a+=b;
    if (a>=MOD2) a-=MOD2;
    return a;
}

int sub(int a, int b) {
    a-=b;
    if (a<0) a+=MOD2;
    return a;
}

int mul(int a, int b) {
    return (int)((a*1LL*b)%MOD2);
}

int binpow(int a, int b) {
    int res = 1;
    while (b) {
        if (b&1) res = mul(res, a);
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}

int inv(int a) {
    return binpow(a, MOD2-2);
}

int di(int a, int b) {
    return mul(a, inv(b));
}

int N;
int a[200005];
bool vis[200005];
map<int,int> m;

void add(int x) {
    for (int i = 2; i*i <= x; i++) {
        if (x%i==0) {
            int cnt = 0;
            while (x%i==0) {
                x /= i;
                cnt++;
            }
            ckmax(m[i], cnt);
        }
    }
    if (x > 1) {
        ckmax(m[x], 1);
    }
}

int get() {
    int ans = 1;
    each(i, m) {
        ans = mul(ans, binpow(i.fi, i.se));
    }
    return ans;
}

