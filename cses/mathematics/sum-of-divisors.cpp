ll ad(ll a, ll b) {
    a+=b;
    if (a>=MOD2) a-=MOD2;
    return a;
}

ll sub(ll a, ll b) {
    a-=b;
    if (a<0) a+=MOD2;
    return a;
}

ll mul(ll a, ll b) {
    return ((a*1LL*b)%MOD2);
}

ll binpow(ll a, ll b) {
    ll res = 1;
    while (b) {
        if (b&1) res = mul(res, a);
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}

ll inv(ll a) {
    return binpow(a, MOD2-2);
}

ll di(ll a, ll b) {
    return mul(a, inv(b));
}

