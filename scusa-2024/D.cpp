template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int N;
bool vis[2005];

int solve(vt<int> &a, vt<int> &b) {
    int j = 0;
    int res = 0;
    for (int i = 0; i < sz(a); i++) {
        while (j < sz(b) && b[j] < a[i]) j++;
        if (j == sz(b)) break;
        res++;
        j++;
    }
    return res;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cin >> N;
    vt<int> a, b;
    for (int i = 0; i < N; i++) {
        int x; cin >> x;
        a.pb(x);
        vis[x] = 1;
    }
    sort(all(a));
    for (int i = 1; i <= 2*N; i++) {
        if (!vis[i]) b.pb(i);
    }
    int ans = N-solve(a, b);
    int ans2 = solve(b, a);
    cout << ans << " " << ans2 << endl;
    return 0;
}