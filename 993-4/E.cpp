ll solve(ll x) {
    ll l = (l2+x-1)/x;
    ll r = r2/x;
    ll L = max(l, l1);
    ll R = min(r, r1);
    if (L > R) return 0;
    // cout << "x: " << x << " L: " << L << " R: " << R << endl;
    return (R-L+1);
}

