string num[] = {"la", "le", "lon", "sha", "she", "shon", "ta", "te", "ton"};

string to9(ll x) {
    vt<string> v;
    while (x) {
        v.pb(num[x % 9]);
        x /= 9;
    }
    reverse(all(v));
    string s;
    for (auto &i : v) {
        s += i;
    }
    s += "s";
    return s;
}

ll parse(string s) {
    int n = sz(s);
    int i = 0;
    ll res = 0;
    while (i < n-1) {
        for (int j = 0; j < 9; j++) {
            if (i+sz(num[j]) <= n && s.substr(i, sz(num[j])) == num[j]) {
                i += sz(num[j]);
                res = res * 9 + j;
                break;
            }
        }
    }
    return res;
}

