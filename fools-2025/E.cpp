ll ad(ll x, ll y) {
    return (x + y) % P;
}

ll mul(ll x, ll y) {
    return (x * y) % P;
}

ll pow(ll x, ll y) {
    if (x == 0) return 0;
    ll res = 1;
    while (y) {
        if (y & 1) res = mul(res, x);
        x = mul(x, x);
        y >>= 1;
    }
    return res;
}

ll inv(ll x) {
    return pow(x, P - 2);
}

ll di(ll x, ll y) {
    return mul(x, inv(y));
}

ll three(ll x) {
    return mul(x, mul(x, x));
}

