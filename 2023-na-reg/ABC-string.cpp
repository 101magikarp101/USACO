template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    string s; cin >> s;
    vt<int> v(7,0);
    int ans = 0;
    for (char c : s) {
        int val = c-'A';
        bool g = 0;
        for (int i = 6; i >= 0; i--) {
            if ((i&(1<<val))==0 && v[i]>0) {
                v[i]--;
                int next = i|(1<<val);
                if (next == 7) {
                    v[0]++;
                } else {
                    v[next]++;
                }
                g = 1;
                break;
            }
        }
        if (!g) {
            v[1<<val]++;
            ans++;
        }
    }
    cout << ans << endl;
    return 0;
}