template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int T, N;
bool a[100005], b[100005];
vt<int> adj[100005];

pii solve(int u, int p, int d) {
    pii ans = {0, 0};
    each(v,adj[u]) {
        if (v == p) continue;
        pii res = solve(v, u, d+1);
        if (d%2 == 0) {
            if (res.x > ans.x) {
                ans = res;
            } else if (res.x == ans.x && res.y > ans.y) {
                ans = res;
            }
        } else {
            if (res.y > ans.y) {
                ans = res;
            } else if (res.y == ans.y && res.x > ans.x) {
                ans = res;
            }
        }
    }
    ans.x += a[u];
    ans.y += b[u];
    return ans;
}

