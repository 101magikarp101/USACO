template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

struct edge {
    int v;
    ll w;
};

int T, N;
ll a[100005];
vt<edge> adj[100005];
ll tot[100005];
ll ans = 0;

void dfs(int u, int p) {
    tot[u] += a[u];
    each(e,adj[u]) {
        if (e.v == p) continue;
        dfs(e.v, u);
        tot[u] += tot[e.v];
        ans += e.w * abs(tot[e.v]);
    }
}

