template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int T, N;
int a[200005];

bool check(int n, int m) {
    for (int i = n; i < m; i++) {
        if (a[i] > a[i+1]) return 0;
    }
    return 1;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cin >> T;
    while (T--) {
        cin >> N;
        for (int i = 0; i < N; i++) {
            cin >> a[i];
        }
        int ans = N;
        for (int i = 0; i < N; i++) {
            for (int j = i; j < N; j++) {
                if (check(i, j)) {
                    ckmin(ans, N-(j-i+1));
                }
            }
        }
        cout << ans << endl;
    }
    return 0;
}