int Q;

int hbit(int x) {
    return 31 - __builtin_clz(x);
}

int up(int x) {
    int h = hbit(x);
    if (x == 1) return -1;
    return x^(1<<h)|(1<<(h-1));
}

int down(int x, int n, int s) {
    int h = hbit(x);
    int r = x^(1<<h);
    int cnt = (n-r)/(1<<h);
    int ans1 = r^(1<<(h+1));
    int ans2 = x^(1<<(h+1));
    if (cnt%2==1) swap(ans1, ans2);
    if (s==1) swap(ans1, ans2);
    if (ans1 > n) return -1;
    else return ans1;
}

