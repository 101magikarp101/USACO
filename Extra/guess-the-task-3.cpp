vt<string> split(string s, string delim) {
    vt<string> res;
    int start = 0;
    int end = s.find(delim);
    while (end != string::npos) {
        res.pb(s.substr(start, end-start));
        start = end+delim.size();
        end = s.find(delim, start);
    }
    res.pb(s.substr(start, end-start));
    return res;
}

