template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

ll T, R, W, H;

ll _sqrtl(ll x, ll y) {
    ll ans = sqrtl(x)/y;
    if (ans*ans*y*y > x) {
        ans--;
    }
    return ans;
}

// res
ll solvex(ll x) {
    ll y = _sqrtl(R*R - x*x*H*H, W);
    // x = _sqrtl(R*R - y*y*W*W, H);
    return x*y;
}

ll solvey(ll y) {
    ll x = _sqrtl(R*R - y*y*W*W, H);
    // y = _sqrtl(R*R - x*x*H*H, W);
    return x*y;
}

