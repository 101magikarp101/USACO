int ad(int a, int b) {
    a+=b;
    if (a>=MOD) a-=MOD;
    return a;
}

int sub(int a, int b) {
    a-=b;
    if (a<0) a+=MOD;
    return a;
}

int mul(int a, int b) {
    return (int)((a*1LL*b)%MOD);
}

int binpow(int a, int b) {
    int res = 1;
    while (b) {
        if (b&1) res = mul(res, a);
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}

int inv(int a) {
    return binpow(a, MOD-2);
}

int di(int a, int b) {
    return mul(a, inv(b));
}

struct Node {
    const pii DEFAULT = {1,0};
    Node *lc = nullptr, *rc = nullptr;
    int s, e;
    pii val = DEFAULT;
    Node(int s, int e): s(s), e(e) {}
    pii merge(pii &a, pii &b) {
        return {mul(a.x, b.x), ad(mul(a.y, b.x), b.y)};
    }
    void extend() {
        if (!lc && s < e) {
            int mid = (s + e) / 2;
            lc = new Node(s, mid);
            rc = new Node(mid + 1, e);
        }
    }
    void set(int i, pii v) {
        if (s == e) {
            val = v;
        } else {
            extend();
            int mid = (s + e) / 2;
            if (i <= mid) {
                lc->set(i, v);
            } else {
                rc->set(i, v);
            }
            val = merge(lc->val, rc->val);
        }
    }
    pii query(int l, int r) {
        if (l > e || r < s) return DEFAULT;
        if (l <= s && e <= r) return val;
        extend();
        pii left = lc->query(l, r);
        pii right = rc->query(l, r);
        return merge(left, right);
    }
};

