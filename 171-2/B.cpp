template<class T> bool ckmin(T& a, const T& b) {
    return b < a ? a = b, 1 : 0; }
template<class T> bool ckmax(T& a, const T& b) {
    return a < b ? a = b, 1 : 0; }

int T, N;
ll a[2005];

bool solve(ll m) {
    if (N%2==0) {
        for (int i = 0; i < N; i+=2) {
            if (a[i+1]-a[i] > m) return 0;
        }
        return 1;
    } else {
        bool used = 0;
        for (int i = 0; i < N; i+=2) {
            if (a[i+1]-a[i] > m) {
                i--;
                if (used) return 0;
                used = 1;
            }
        }
        return 1;
    }
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cin >> T;
    while (T--) {
        cin >> N;
        for (int i = 0; i < N; i++) cin >> a[i];
        ll l = 1, r = 1e18;
        while (l < r) {
            ll m = (l+r)/2;
            if (solve(m)) r = m;
            else l = m+1;
        }
        cout << l << endl;
    }
    return 0;
}